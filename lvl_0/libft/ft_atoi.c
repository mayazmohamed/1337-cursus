#include"libft.h"

int	ft_atoi(const char *nptr)
{
	int	a;
	int	b;

	a = 0;
	b = 1;
	while ((*nptr >= 9 && *nptr <= 13) || *nptr == 32)
		nptr++;
	if (*nptr == '-' || *nptr == '+')
	{
		if (*nptr == '-')
			b = -b;
		nptr++;
	}
	while (*nptr >= '0' && *nptr <= '9')
	{
		a = a * 10 + *nptr - 48;
		nptr++;
	}
	return (a * b);
}
